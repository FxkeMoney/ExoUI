local Icons = {}

function Icons:Create(nameOrId)
	local iconData
	local isAssetId = tonumber(nameOrId) ~= nil
	
	if isAssetId then
		iconData = {
			Type = "AssetId",
			Id = tostring(nameOrId),
		}
	else
		local icon = IconLibrary[nameOrId]
		if not icon then
			error(("Unknown icon name: %s"):format(tostring(nameOrId)))
		end
		iconData = {
			Type = "NamedIcon",
			Name = nameOrId,
			Definition = icon,
		}
	end
	return setmetatable(iconData, { __type = "Icon" })
end

return library, Icons
