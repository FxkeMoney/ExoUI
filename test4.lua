function library:AddBlur()
    for i,v in pairs(game:GetService("Lighting"):GetChildren()) do
        if v.Name == "Blur" then
            v:Destroy()
        end
    end
    local blur = Instance.new("BlurEffect", game:GetService("Lighting"))
end
function library:RemoveBlur()
    for i,v in pairs(game:GetService("Lighting"):GetChildren()) do
        if v.Name == "Blur" then
            v:Destroy()
        end
    end
    if game:GetService("Lighting"):FindFirstChild("Blur") then
        local blur = game:GetService("Lighting")["Blur"]
        game:GetService("TweenService"):Create(blur, TweenInfo.new(0.3), {Size=0}):Play()
        wait(0.2)
        blur:Destroy()
    end
end
function library:CloseUI()
    library.active = false
    library:RemoveBlur()
    for i,v in pairs(ModalFolder:GetChildren()) do
        game:GetService("TweenService"):Create(v.scale, TweenInfo.new(0.3), {Scale=0}):Play()
    end
    game:GetService("TweenService"):Create(frame.scale, TweenInfo.new(0.3), {Scale=0}):Play()
    wait(0.2)
    ScreenGui.Enabled = false
end
function library:OpenUI()
    library:AddBlur()
    wait(0.05)
    library.active = true
    ScreenGui.Enabled = true
    game:GetService("TweenService"):Create(frame.scale, TweenInfo.new(0.5), {Scale=1}):Play()
    for i,v in pairs(ModalFolder:GetChildren()) do
        game:GetService("TweenService"):Create(v.scale, TweenInfo.new(0.5), {Scale=1}):Play()
    end
    wait(0.2)
    ScreenGui.Enabled = true
end
function library:ToggleUI()
    if library.active then
        library:CloseUI()
    else 
        library:OpenUI()
    end
end
